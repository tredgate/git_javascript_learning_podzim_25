console.log("string"); // ? Textová hodnota
console.log(12); // ? Typ: number (číslo)
console.log(15.74); // ? Typ: number (číslo) - píšeme desetinnou tečku
console.log("12"); // ! Není number, ale string - způsobí problémy!
console.log("12" + 12); // ! Není doporučeno, nekombinujeme různé datové typy
console.log(12 + "12");
console.log("12" - 10);
console.log("12a" - 10);
console.log(0.2 * 0.1);

console.log(true); // ? Typ: boolean (logická hodnota)
console.log(false); // ? Typ: boolean (logická hodnota)

console.log(undefined);
console.log(null);
